package sk.sosholic.opakovanie;

public class DatoveTypy {
	public static void main(String[] args) {
		System.out.println("byte uchovava prirodzene cislo velkosti 8b");
		System.out.println("short uchovava prirodzene cislo velkosti 16b");
		System.out.println("char uchovava string velkosti 16b");
		System.out.println("int uchovava prirodzene cislo velkosti 32b");
		System.out.println("float uchovava celé aj desatinné cislo velkosti 32b");
		System.out.println("double uchovava prirodzene cislo velkosti 64b");
		System.out.println("boolean dáva hodnoty true/false");
	}
}
